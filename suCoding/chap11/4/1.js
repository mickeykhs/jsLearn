/* 브라우저 객체 모델 사용하기 */
/* 
    window : 웹 브라우저가 열릴 때마다 생성되는 최상위 객체
    document : 웹 브라우저에 표시되는 HTML 문서 정보가 포함된 객체
    location : 웹 브라우저에 현재 표시된 페이지에 대한 URL 정보가 포함된 객체
    history : 웹 브라우저에 저장된 방문 기록이 포함된 객체
    navigator : 웹 브라우저 정보가 포함된 객체
    screen : 웹 브라우저 화면 정보가 포함된 객체
*/
/* window 객체의 속성과 메서드
    -----------속성----------
    innerWidth : 웹 브라우저 화면의 너비를 px단위로 나타냄
    innerHeight : 화면의 높이를 px단위로 나타냄
    outerWidth : 창의 너비를 px단위로 나타냄
    outerHeight : 창의 높이를 px단위로 나타냄
    screenTop/screenY : 위쪽 면과 모니터의 간격을 px단위로 나타냄
    screenLeft/screenX : 왼쪽 면과 모니터의 간격을 px단위로 나타냄
    ----------메서드----------
    alert() : 알림창 표시
    confirm() : 확인창 표시
    prompt() : 입력창 표시
    open() : 새로운 웹 브라우저 창을 염
    close() : 웹 브라우저 창을 닫음
    setTimeout() : 일정 시간(ms)뒤에 콜백 함수를 한번만 실행
    setInterval() : 일정 시간(ms)마다 콜백 함수를 반복적으로 실행
    clearInterval() : setInterval() 메서드로 반복 실행되는 함수를 중지
    scrollTo() : 웹 브라우저의 스크롤을 특정 위치만큼 이동
    scrollBy() : 웹 브라우저의 스크롤을 현재 위치에서 상대적 위치로 이동
*/
